<p>
    D1SCO is a platform for communities centred around music,
    allowing users to share, discuss and discover the music they love.
</p>
<p>
    This project was one which we at 2V3.studio came up with ourselves.
    Our main priority when building the app was to create a working
    version in a short period of time. It only took us 3 weeks from outlining the
    idea to having initial users on the platform!
</p>
<img src="D1scoPreview.png" alt="Screenshot of D1sco website">
<h3>Design</h3>
<p>
    We followed our standard design process, first establishing the core functionality of the prototype.
    This is something we usually do over video calls
    with clients. Our specification for the D1SCO platform was:
    <li>Log-in and sign-up system</li>
    <li>The ability to search for any song, album or artist</li>
    <li>A way of sharing a song/album/artist with a text caption</li>
    <li>Likes and threaded comments on posts</li>
</p>
<p>
    Once we'd finalised our specification, we went on to wireframe the web app.
</p>
<blockquote>
    A wireframe is a skeletal outline of a digital
    interface or design, illustrating the layout and basic elements without
    detailed design elements.
</blockquote>
<h3>Develop</h3>
<p>
    To develop the web app, we used the following tech stack:
    <li>SvelteKit Frontend</li>
    <li>Firebase Backend</li>
    <li>Vercel Hosting</li>
</p>
<p>
    To provide a library of songs, albums and artists that users could share within the app, we used the <a
        href="https://developer.spotify.com/documentation/web-api"
        >Spotify Api</a
    >. By connecting our service to Spotify, we were able to introduce full-text song search, and 
    display an artists most streamed songs.
</p>
<h3>Release</h3>
<p>
    We decided to host D1SCO through Vercel using a testing url <a href="/"
        >www.D1SCO.vercel.app</a
    >. This allowed us to publish the website live and get user feedback before committing to a name and
    brand.
</p>
<p>
    When we work with clients, we will host their website on a testing url so that
    they can review the website before it gets published live to their domain.
</p>
